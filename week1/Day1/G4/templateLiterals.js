// Topic: Template Literals

const name = "Abdullah";

// Using template literals
console.log(`Hello ${name}!`); // Output: Hello Abdullah!


// String concatenation (ES5)

const name2 = "Abdullah";

console.log("Hello " + name2 + "!");

// Template literals (ES6)

const name3 = "Abdullah";

console.log(`Hello ${name3}!`);

// Using multiple variables

const product = "Laptop";
const price = 50000;

console.log(`The price of ${product} is Rs. ${price}.`);//The price of Laptop is Rs. 50000.


// Multi-line string

const message = `
Welcome to JavaScript.
Template literals support
multiple lines.
`;

console.log(message);

